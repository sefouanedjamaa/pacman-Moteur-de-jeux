function Mange(radius)
{
    this.radius = radius,

    this.scoreValue =   1,

    this.affiche = function(x, y)
    {
        jeu.ctx.fillStyle = "#F00";
        jeu.ctx.beginPath();
        jeu.ctx.arc(x, y, this.radius, 2, Math.PI * 360);
        jeu.ctx.fill();
    };
}
